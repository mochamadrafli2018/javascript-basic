const orderTea = () => {
    let tea = null;
    console.log("Making a tea, please wait for a minute ...");
    setTimeout(() => {
        tea = "Tea is here!";
    }, 3000);
    return tea;
}
 
const tea = orderTea();
console.log(tea);
 
/* output
Making a tea, please wait for a minute ...
null
*/

//null because return tea will be executed earlier than tea = "Tea is here!"
